public class ExampleVO {

	public String EMP_NO;
	public String EMP_NAME;
	public String EMP_AGE;
	public String EMP_EMAIL;
	public String DEPT_CODE;
	public String TITLE_CODE;
	public String MAIL_ADDR;
	
	
	public String getEMP_NO() {
		return EMP_NO;
	}
	public void setEMP_NO(String emp_no) {
		EMP_NO = emp_no;
	}
	public String getEMP_NAME() {
		return EMP_NAME;
	}
	public void setEMP_NAME(String emp_name) {
		EMP_NAME = emp_name;
	}
	public String getEMP_AGE() {
		return EMP_AGE;
	}
	public void setEMP_AGE(String emp_age) {
		EMP_AGE = emp_age;
	}
	public String getEMP_EMAIL() {
		return EMP_EMAIL;
	}
	public void setEMP_EMAIL(String emp_email) {
		EMP_EMAIL = emp_email;
	}
	public String getDEPT_CODE() {
		return DEPT_CODE;
	}
	public void setDEPT_CODE(String dept_code) {
		DEPT_CODE = dept_code;
	}
	public String getTITLE_CODE() {
		return TITLE_CODE;
	}
	public void setTITLE_CODE(String title_code) {
		TITLE_CODE = title_code;
	}
	public String getMAIL_ADDR() {
		return MAIL_ADDR;
	}
	public void setMAIL_ADDR(String mail_addr) {
		MAIL_ADDR = mail_addr;
	}
	
	
}
